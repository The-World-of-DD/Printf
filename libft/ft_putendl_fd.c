#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	i;

	if (!s)
		return ;
	i = 0;
	while (*s)
	{
		write(fd, &s[i], 1);
		s++;
	}
	write(fd, "\n", 1);
}
